# 문자 선택 연산자
print("hello"[0]) # h
print("hello"[1]) # e
print("hello"[2]) # l
print("hello"[3]) # l
print("hello"[4]) # o
print("hello"[-0]) # h
print("hello"[-1]) # o
print("hello"[-2]) # l
print("hello"[-3]) # l
print("hello"[-4]) # e
print("hello"[-5]) # h
